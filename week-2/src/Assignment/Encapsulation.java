package Assignment;

public class Encapsulation {
	
	    private String khinName; 
	    private int roll; 
	    private int age; 
	  
	   
	    public int getAge()  
	    { 
	      return age; 
	    } 
	   
	    
	    public String getName()  
	    { 
	      return khinName; 
	    } 
	      
	    
	    public int getRoll()  
	    { 
	       return roll; 
	    } 
	   
	    
	    public void setAge( int newAge) 
	    { 
	      age = newAge; 
	    } 
	   
	  
	    public void setName(String newName) 
	    { 
	      khinName = newName; 
	    } 
	      
	   
	    public void setRoll( int newRoll)  
	    { 
	      roll = newRoll; 
	    } 
	} 


